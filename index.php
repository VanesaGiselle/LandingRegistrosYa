		  // prevenir que se dispare el evento click 
		  event.preventDefault(); 
		  // guardar hash
		  var hash = this.hash; 
		  // con el método animate() de la librería de jQuery suavizamos el scroll
		  // el valor opcional (900) es el número en milisegundos que le toma en llegar al area especificada 
		  $('html, body').animate({
			scrollTop: $(hash).offset().top
		  }, 900, function(){ 
			// Agregar el valor hash (#) en la URL cuando termine el scroll
			window.location.hash = hash;
		  });
		} 
	  });
	})
